public class Sueldo {
    private int idSueldo;
    private int salarioMinimo;

    /**
     * @return the idSueldo
     */
    public int getIdSueldo() {
        return idSueldo;
    }

    /**
     * @param idSueldo the idSueldo to set
     */
    public void setIdSueldo(int idSueldo) {
        this.idSueldo = idSueldo;
    }

    /**
     * @return the salarioMinimo
     */
    public int getSalarioMinimo() {
        return salarioMinimo;
    }

    /**
     * @param salarioMinimo the salarioMinimo to set
     */
    public void setSalarioMinimo(int salarioMinimo) {
        this.salarioMinimo = salarioMinimo;
    }
    
    
}
