package mx.edu.tecnm.toluca.model;

/**
 *
 * @author Pablo RosHdz
 */
public class Credito {
    private int idCredito;
    private int limiteCredito;
    private int deudaCredito;

    /**
     * @return the idCredito
     */
    public int getIdCredito() {
        return idCredito;
    }

    /**
     * @param idCredito the idCredito to set
     */
    public void setIdCredito(int idCredito) {
        this.idCredito = idCredito;
    }

    /**
     * @return the limiteCredito
     */
    public int getLimiteCredito() {
        return limiteCredito;
    }

    /**
     * @param limiteCredito the limiteCredito to set
     */
    public void setLimiteCredito(int limiteCredito) {
        this.limiteCredito = limiteCredito;
    }

    /**
     * @return the deudaCredito
     */
    public int getDeudaCredito() {
        return deudaCredito;
    }

    /**
     * @param deudaCredito the deudaCredito to set
     */
    public void setDeudaCredito(int deudaCredito) {
        this.deudaCredito = deudaCredito;
    }
    
    
}
